def add_defer_meth(cls, methname: str, defertoname: str, /):
    exec('\n'.join((
        f"@property",
        f"def {methname}(self, /):"
        f"    return self.{defertoname}.{methname}"
        )))
    setattr(cls, methname, eval(methname))


